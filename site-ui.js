function setActiveLanguage() {
  const language = getLanguage();
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === language);
  });
}

function injectPricingPopup() {
  if (document.querySelector(".pricing-popup")) return;
  const popup = document.createElement("aside");
  popup.className = "pricing-popup";
  popup.setAttribute("aria-live", "polite");
  popup.innerHTML = `
    <button class="pricing-close" type="button" aria-label="Close pricing popup" data-i18n="pricingClose">Close</button>
    <span data-i18n="pricingBadge">Pricing help</span>
    <strong data-i18n="pricingTitle">Want a real number before the visit?</strong>
    <p data-i18n="pricingText">Call us and we will narrow the likely repair or heat pump budget before we send a technician.</p>
    <div>
      <a class="button primary small" href="tel:+17813811212" data-i18n="pricingCall">Call for Pricing</a>
      <button class="button secondary small" type="button" data-open-advisor data-i18n="pricingAdvisor">Use ComfortFit AI</button>
    </div>
  `;
  document.body.appendChild(popup);
  const dismissed = sessionStorage.getItem("baystate-pricing-dismissed");
  if (!dismissed) {
    window.setTimeout(() => popup.classList.add("is-visible"), 1800);
  }
  popup.querySelector(".pricing-close").addEventListener("click", () => {
    popup.classList.remove("is-visible");
    sessionStorage.setItem("baystate-pricing-dismissed", "1");
  });
  popup.querySelector("[data-open-advisor]")?.addEventListener("click", () => {
    popup.classList.remove("is-visible");
    const advisorTrigger = document.querySelector(".hero [data-open-advisor], .advisor-card [data-open-advisor]");
    if (advisorTrigger) {
      advisorTrigger.click();
    } else {
      window.location.href = "index.html#advisor";
    }
  });
}

